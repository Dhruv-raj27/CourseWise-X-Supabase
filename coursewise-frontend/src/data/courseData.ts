export const filterCourses = (
  courses: Course[],
  semester: number,
  branch: string,
  includeOnline: boolean = true
) => {
  const regularCourses = courses.filter(course => 
    (course.semester === semester) &&
    (course.stream === branch || course.stream === 'All')
  );

  if (!includeOnline) return regularCourses;

  return regularCourses;
};

